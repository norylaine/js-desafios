// Faça um programa que receba um valor em metros e o exiba convertido para centímetros e milímetros.

// ENTRADA
let metros = Number(prompt("Digite um valor em metros"))

// PROCESSAMENTO
let centimetros = metros * 100
let milimetros = centimetros * 10

// SAÍDA
console.log(metros + " metros equivale a " + centimetros + " centímetros e " + milimetros + " milímetros ")