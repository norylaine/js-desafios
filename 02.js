// Desafio 2:
// Crie um programa que receba dois números e imprima na tela a soma entre eles.

// Entrada 
// Receber os dois números

let numero1 = Number(prompt("Digite o primeiro número: "))
let numero2 = Number(prompt("Digite o segundo número: "))

// Processamento
// A operação matemática
let resultado = numero1 + numero2

// Saída
// Exibir na tela o resultado
console.log("A soma entre " + numero1 + " e " + numero2 + " é: " + resultado)